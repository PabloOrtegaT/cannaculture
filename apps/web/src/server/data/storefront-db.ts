type CatalogFixtureInput = {
  categoryId: string;
  productSeed: number;
  variantSeed: number;
  name: string;
  slug: string;
  description: string;
  baseSku: string;
  priceCents: number;
  compareAtPriceCents: number;
  tags: string[];
  variantSku: string;
  variantName: string;
  stockOnHand: number;
  attributeValues: Record<string, AttributeValue>;
  createdAt?: string;
  updatedAt?: string;
};

function fixtureUuid(seed: number) {
  return `10000000-0000-4000-8000-${seed.toString().padStart(12, "0")}`;
}

function buildFixtureTimestamp(month: "02" | "03" | "04", day: number, hour: number) {
  return `2026-${month}-${String(day).padStart(2, "0")}T${String(hour).padStart(2, "0")}:00:00.000Z`;
}

function buildCatalogFixtures(fixtures: CatalogFixtureInput[]) {
  return {
    products: fixtures.map((fixture, index) =>
      productSchema.parse({
        id: fixtureUuid(fixture.productSeed),
        categoryId: fixture.categoryId,
        name: fixture.name,
        slug: fixture.slug,
        description: fixture.description,
        baseSku: fixture.baseSku,
        status: "active",
        currency: "MXN",
        priceCents: fixture.priceCents,
        compareAtPriceCents: fixture.compareAtPriceCents,
        tags: fixture.tags,
        createdAt: fixture.createdAt ?? buildFixtureTimestamp("02", index + 1, 10),
        updatedAt: fixture.updatedAt ?? buildFixtureTimestamp("03", index + 1, 9),
      }),
    ),
    variants: fixtures.map((fixture, index) =>
      productVariantSchema.parse({
        id: fixtureUuid(fixture.variantSeed),
        productId: fixtureUuid(fixture.productSeed),
        sku: fixture.variantSku,
        name: fixture.variantName,
        priceCents: fixture.priceCents,
        compareAtPriceCents: fixture.compareAtPriceCents,
        stockOnHand: fixture.stockOnHand,
        isDefault: true,
        attributeValues: fixture.attributeValues,
        createdAt: fixture.createdAt ?? buildFixtureTimestamp("02", index + 1, 10),
        updatedAt: fixture.updatedAt ?? buildFixtureTimestamp("03", index + 1, 9),
      }),
    ),
  };
}

